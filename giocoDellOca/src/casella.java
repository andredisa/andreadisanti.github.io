public class casella {
    public int valore;
    public boolean occupata;

    public casella(){
        valore=0;
        occupata=false;
    }

    public void setValore(int bonus) {
        this.valore = bonus;
    }

    public void setOccupata(boolean occupata) {
        this.occupata = occupata;
    }
    
}
